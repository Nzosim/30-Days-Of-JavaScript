const date = new Date()
console.log(date) // 2022-04-01T20:28:18.196Z
console.log(date.getFullYear()) // 2022
console.log(date.getMonth()) // 3 = avril
console.log(date.getDate()) // 1 
console.log(date.getDay()) // 5 = friday
console.log(date.getHours()) // 22
console.log(date.getMinutes()) // 31
console.log(date.getSeconds()) // 2
console.log(date.getTime()) // 1648845134302 = number of seconds from 01/01/1970 to now = Date.now()
